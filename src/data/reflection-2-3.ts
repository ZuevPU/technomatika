export function zoneFor(value: number): ScaleZone {
  if (value <= 3) return 'gap';
  if (value <= 7) return 'transition';
  return 'anchor';
}

export function zoneLabel(zone: ScaleZone): string {
  if (zone === 'gap') return 'Зона осознания';
  if (zone === 'transition') return 'Зона баланса';
  return 'Зона мастерства';
}

export function meanScore(values: number[]): number {
  const sum = values.reduce((a, b) => a + b, 0);
  return Math.round((sum / values.length) * 10) / 10;
}

export function pickConclusion(mean: number): ConclusionTemplate {
  if (mean <= 3.9) return CONCLUSIONS[0];
  if (mean <= 6.9) return CONCLUSIONS[1];
  return CONCLUSIONS[2];
}

export function topAxes(
  values: number[],
  predicate: (v: number) => boolean,
  limit = 3
): string[] {
  return SCALES.map((scale, i) => ({ axis: scale.axis, value: values[i] }))
    .filter((item) => predicate(item.value))
    .sort((a, b) => Math.abs(b.value - 5) - Math.abs(a.value - 5))
    .slice(0, limit)
    .map((item) => item.axis);
}
