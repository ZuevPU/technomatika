export function levelFor(score: number): { label: string; hint: string } {
  for (const tier of LEVEL_LABELS) {
    if (score <= tier.max) return { label: tier.label, hint: tier.hint };
  }
  return { label: LEVEL_LABELS[LEVEL_LABELS.length - 1].label, hint: LEVEL_LABELS[LEVEL_LABELS.length - 1].hint };
}

export function blockAverage(values: number[], blockId: string): number {
  const items = STATEMENTS.filter((s) => s.blockId === blockId).map((s) => values[s.id - 1]);
  const sum = items.reduce((a, b) => a + b, 0);
  return Math.round((sum / items.length) * 10) / 10;
}

export function allBlockAverages(values: number[]): { block: SkillBlock; score: number }[] {
  return BLOCKS.map((block) => ({ block, score: blockAverage(values, block.id) }));
}

export function pickConclusion(blockScores: Record<string, number>): ConclusionTemplate {
  const scores = BLOCKS.map((b) => blockScores[b.id]);
  const allLow = scores.every((s) => s <= 2.5);
  const allHigh = scores.every((s) => s >= 4);
  const aHigh = blockScores.A >= 3.6;
  const cHigh = blockScores.C >= 3.6;
  const bLow = blockScores.B <= 2.5;
  const dLow = blockScores.D <= 2.5;
  const eLow = blockScores.E <= 2.5;

  if (allHigh) return CONCLUSIONS[2];
  if (allLow) return CONCLUSIONS[0];
  if (aHigh && cHigh && bLow && dLow) return CONCLUSIONS[1];
  if (aHigh && eLow) return CONCLUSIONS[3];
  return CONCLUSIONS[4];
}

export function weakestInBlock(values: number[], blockId: string, limit = 2): string[] {
  return STATEMENTS.filter((s) => s.blockId === blockId)
    .map((s) => ({ text: s.text, value: values[s.id - 1] }))
    .sort((a, b) => a.value - b.value)
    .slice(0, limit)
    .map((item) => item.text);
}
